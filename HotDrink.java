public class HotDrink {

    private String name;
    private int vol;

    public HotDrink(String name, int vol) {
        this.name = name;
        this.vol = vol;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getVol() {
        return vol;
    }

    public void setVol(int vol) {
        this.vol = vol;
    }
}


    